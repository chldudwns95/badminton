package com.bdmt.bdmt.model;

public class Team {
	String TEAM_ID;
	String GAME_ID;
	String USER_ID;
	String TEAM_NM;
	String TEAM_M_ID;
	
	public String getTEAM_ID() {
		return TEAM_ID;
	}
	public void setTEAM_ID(String TEAM_ID) {
		TEAM_ID = TEAM_ID;
	}
	public String getGAME_ID() {
		return GAME_ID;
	}
	public void setGAME_ID(String GAME_ID) {
		GAME_ID = GAME_ID;
	}
	public String getUSER_ID() {
		return USER_ID;
	}
	public void setUSER_ID(String USER_ID) {
		USER_ID = USER_ID;
	}
	public String getTEAM_NM() {
		return TEAM_NM;
	}
	public void setTEAM_NM(String TEAM_NM) {
		TEAM_NM = TEAM_NM;
	}
	public String getTEAM_M_ID() {
		return TEAM_M_ID;
	}
	public void setTEAM_M_ID(String TEAM_M_ID) {
		TEAM_M_ID = TEAM_M_ID;
	}
	
	
}
